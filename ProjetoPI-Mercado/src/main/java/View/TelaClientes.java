    private void btn_BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_BuscarActionPerformed
        // TODO add your handling code here:
        if (txt_CampoBusca.getText().strip().equals("cpf")) {
            atualizarTabela();
        } else {
            String cpfx = txt_CampoBusca.getText().trim();
            ArrayList<Cliente> lstRetorno = ClienteDAO.buscarNumero(cpfx);

            DefaultTableModel modelo = (DefaultTableModel) tblClientes.getModel();

            modelo.setRowCount(0);

            for (Cliente item : lstRetorno) {
                //adicionando uma linha a tabela
                modelo.addRow(new String[]{
                    String.valueOf(item.getNome()),
                    String.valueOf(item.getCPF()),
                    String.valueOf(item.getCEP()),
                    String.valueOf(item.getCidade()),
                    String.valueOf(item.getEstado()),
                    String.valueOf(item.getRua()),
                    String.valueOf(item.getNumero()),
                    String.valueOf(item.getEmail()),
                    String.valueOf(item.getTelefone()),
                    String.valueOf(item.getDatanascimento()),
                    String.valueOf(item.getGenero()),
                    String.valueOf(item.getEstadoCivil()),});
            }
        }
    }//GEN-LAST:event_btn_BuscarActionPerformed

    public void atualizarTabela() {
        //chama a DAO para listar as notas adicionadas
        ArrayList<Cliente> lstRetorno = ClienteDAO.listar();

        //para cada item da lstRetorno adicionio a tabela
        DefaultTableModel modelo = (DefaultTableModel) tblClientes.getModel();

        //Zerar as linhas da tabela
        modelo.setRowCount(0);

        for (Cliente item : lstRetorno) {
            //adicionando uma linha a tabela
            modelo.addRow(new String[]{
                String.valueOf(item.getNome()),
                String.valueOf(item.getCPF()),
                String.valueOf(item.getCEP()),
                String.valueOf(item.getCidade()),
                String.valueOf(item.getEstado()),
                String.valueOf(item.getRua()),
                String.valueOf(item.getNumero()),
                String.valueOf(item.getEmail()),
                String.valueOf(item.getTelefone()),
                String.valueOf(item.getDatanascimento()),
                String.valueOf(item.getGenero()),
                String.valueOf(item.getEstadoCivil()),});
        }
    }

