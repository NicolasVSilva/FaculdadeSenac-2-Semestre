    private void btn_EditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_EditarActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = tblClientes.getSelectedRow();

        if (linhaSelecionada >= 0) {
            DefaultTableModel modelo = (DefaultTableModel) tblClientes.getModel();

            String nome = modelo.getValueAt(linhaSelecionada, 0).toString();
            String cpf = (modelo.getValueAt(linhaSelecionada, 1).toString());
            String cep = (modelo.getValueAt(linhaSelecionada, 2).toString());
            String cidade = modelo.getValueAt(linhaSelecionada, 3).toString();
            String estado = (modelo.getValueAt(linhaSelecionada, 4).toString());
            String rua = (modelo.getValueAt(linhaSelecionada, 5).toString());
            int numeroResidencia = Integer.parseInt(modelo.getValueAt(linhaSelecionada, 6).toString());
            String email = (modelo.getValueAt(linhaSelecionada, 7).toString());
            String telefone = (modelo.getValueAt(linhaSelecionada, 8).toString());
            Date dataNascimento = Date.valueOf(modelo.getValueAt(linhaSelecionada, 9).toString());
            String genero = (modelo.getValueAt(linhaSelecionada, 10).toString());
            String estadoCivil = (modelo.getValueAt(linhaSelecionada, 11).toString());

            Cliente objAlterar = new Cliente(nome, cpf, cep, cidade, estado, rua, email, estadoCivil, genero, numeroResidencia, telefone, dataNascimento);

            //Mandar objeto para tela de cadastro
            TelaCadastroCliente telaCadastro = new TelaCadastroCliente(objAlterar);
            telaCadastro.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione uma linha!");
        }
    }//GEN-LAST:event_btn_EditarActionPerformed

    private void btn_RemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_RemoverActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = tblClientes.getSelectedRow();

        if (linhaSelecionada >= 0) {
            DefaultTableModel modelo = (DefaultTableModel) tblClientes.getModel();

            String cpfx = modelo.getValueAt(linhaSelecionada, 1).toString();

            boolean retorno = ClienteDAO.excluir(cpfx);

            if (retorno) {
                JOptionPane.showMessageDialog(rootPane, "Sucesso!");
                btn_BuscarActionPerformed(evt);
            } else {
                JOptionPane.showMessageDialog(rootPane, "Falha!");
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione uma linha!!");
        }
    }//GEN-LAST:event_btn_RemoverActionPerformed

